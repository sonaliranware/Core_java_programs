package simpleproject;
//assignment operators-----  +=,-=,*=,/=
public class Pro10 {

	public static void main(String[] args) {
		int a=10;
		int r1=a+=20;      //a=a+20;
		int r2=a-=10;      //30-10
		int r3=a*=2;       //20*2=40
		int r4=a/=2;      //40/2--20
		System.out.println(a); 
		System.out.println(r1);
		System.out.println(r2);
		System.out.println(r3);
		System.out.println(r4);

	}

}
