package simpleproject;
//relational operator----- >,<,>=,<=,==,!=
public class Pro8 {

	public static void main(String[] args) {
	int a=10,b=20,c=10;
	System.out.println(a>b);  //false
	System.out.println(a<b);
	System.out.println(a>=b);
	System.out.println(a<=b);
	System.out.println(a==c);
	System.out.println(a!=b);

	}

}
